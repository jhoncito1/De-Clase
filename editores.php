            <h1>10 de los mejores editores de texto para programar</h1><br><br>

            <h2>1. Visual Studio Code</h2><br>
            <p> Visual Studio Code está basado en Electron, una pieza de software que se basa en Chromium, utilizada para el desarrollo de aplicaciones de escritorio con io.js. Fue desarrollado por Microsoft, sistemas Windows, Linux y OS X.  </p>
            <img src="img/visual.jpg" alt="">
            <br><br>

            <h2>2. Atom</h2><br>
            <p>  Se autodenomina un editor de texto «para el siglo 21», Atom ha ganado muchos elogios por ser diseñado para las necesidades de los desarrolladores de hoy. Está construido por el equipo de GitHub, e incorpora algunas de las lecciones que el equipo ha aprendido por la gestión de tanto código durante años. Es flexible y personalizable, cuenta con un gran número de seguidores y un montón de plugins, gracias a su API abierta. Funciona como una aplicación nativa, e incluso el paquete de aplicaciones es adaptable.   </p>
            <img src="img/atom.png" alt=""><br><br>

            <h2>3. Sublime Text</h2><br>
            <p> Multiplataforma y con multitud de funciones, Sublime Text es uno de los editores favoritos del público programador, en parte debido a su sorprendente conjunto de características. Plug-ins y complementos que están disponibles para lenguajes de programación específicos, toneladas de comandos de teclado para ayudarlo a que nunca tenga que quitar las manos del mismo mientras lo utiliza, un modo libre de distracciones que le permite centrarse en su trabajo y mucho más.    </p>
            <img src="img/sublime.jpg" alt=""><br><br>

            <h2>4. Brackets</h2><br>
            <p> Brackets es un editor de texto para desarrolladores web, es open source y fue elaborado por la empresa Adobe. Se puede utilizar en plataformas Windows, Linux y Mac OS.    </p>
            <img src="img/brackets.jpg" alt=""><br><br>

            <h2>5. Notepad++</h2><br>
            <p>  Notepad++ combina la sencilla interfaz del Bloc de notas o de Wordpad, con funciones avanzadas que harán a escritores y desarrolladores felices. Algunas de sus características incluyen una interfaz personalizable, que puede hacerla con lo más mínimo o incluir una barra de herramientas enriquecida, un mapa del documento para que pueda ver en dónde va su trabajo, pestañas para que pueda trabajar en varios documentos, autocompletado, grabación de macros, resaltado de sintaxis personalizable, plegado de texto, entre otras.   </p>
            <img src="img/notepad++.jpg" alt=""><br><br>

            <h2>6. Kate</h2><br>
            <p> Kate goza de características excelentes, es fácil de usar y gratuito. Se puede utilizar como un simple editor de texto, con resaltado de sintaxis, o con todas sus características y plugins, logrando casi cualquier cosa que un IDE completo puede hacer. Es el editor perfecto para los que acaban de entrar en el mundo de la programación, puede descubrir rápidamente todas las características impresionantes que tiene que ofrecer sin sentirse abrumado y confundido.    </p>
            <img src="img/kate.jpg" alt=""><br><br>

            <h2>7. Paiza.io</h2><br>
            <p>  Paiza.io es un entorno de codificación basada en la web. Compatible con más de 20 lenguajes de programación como Java, Ruby, Python, PHP, Perl, entre otros, se puede utilizar para aprender a programar, crear bosquejos de sitios web, escribir por lotes y mucho más.   </p>
            <img src="img/paiza.jpg" alt=""><br><br>

            <h2>8. TextMate</h2><br>
            <p> Este editor cuenta con características como autoidentación de acciones comunes (como pegar texto), historial de portapapeles, autocompletado de palabras, selectores CSS, esquema dinámico para trabajar con varios archivos, función Pop-up para navegación rápida, macros que se pueden grabar sin necesidad de programación, ejecución de comandos de terminal dentro del documento, cambio entre los archivos de distintos proyectos con pocos clic, marcadores visuales para saltar entre puntos situados en un archivo, entre otras.    </p>
            <img src="img/texmate.jpg" alt=""><br><br>

            <h2>9. Aptana Studio</h2><br>
            <p> Aptana Studio también es digno de una mirada. Más que nada es un editor para HTML 5, cuenta con un modo live editing, y tiene suficientes opciones de personalización que puede hacerlo llegar, con un poco de trabajo, a que funcione de manera similar a Dreamweaver. También provee soporte para lenguajes como: Php, Python, Ruby, CSS, Ajax, HTML y Adobe AIR.    </p>
            <img src="img/aptana.jpg" alt=""><br><br>

            <h2>10. Nebulous Notes</h2><br>
            <p> Nebulous Notes es una herramienta que le permite la escritura de código a través de dispositivos de pantalla táctil. Es compatible con TextExpander, así que si lo tiene instalado puede utilizar snippets dentro de la aplicación. Además, hay un sistema de macro integrada que puede utilizar para ejecutar rápidamente las pulsaciones del teclado más tediosas. Esas macros se pueden almacenar en la barra de herramientas para un fácil acceso.   </p>
            <img src="img/nebolous.jpg" alt=""><br><br>
   
